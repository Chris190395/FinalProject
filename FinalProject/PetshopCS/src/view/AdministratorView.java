	@Override
	public void FindUser(User user, Boolean error, String errorMsg) {
		// TODO Auto-generated method stub
		if (error == false)
        {
			jTable1.removeAll();
            String[][] obj = new String[1][6];
        	obj[0][0]=""+user.getId();
        	obj[0][1]=user.getUserName();
        	obj[0][2]=user.getPassword();
        	obj[0][3]=user.getFirstName();
        	obj[0][4]=user.getLastName();
        	obj[0][5]=user.getType();
        		
        	jTable1.setModel(new javax.swing.table.DefaultTableModel(
                    obj,
                    new String [] {
                            "Id", "UserName", "Password", "FirstName", "LastName", "Type"
                        }
                ));
			errorUser.setText(errorMsg);
        }
        else
        	errorUser.setText(errorMsg);
	}

	@Override
	public User GetSelectedUserFromGrid() {
		// TODO Auto-generated method stub
		if (jTable1.getSelectedRowCount() > 0)
        {
        	int row = jTable1.getSelectedRow();
        	User user = new User();
        	user.setId(Integer.parseInt(jTable1.getValueAt(row, 0).toString()));
        	user.setUserName(jTable1.getValueAt(row, 1).toString());
        	user.setPassword(jTable1.getValueAt(row, 2).toString());
        	user.setFirstName(jTable1.getValueAt(row, 3).toString());
        	user.setLastName(jTable1.getValueAt(row, 5).toString());
        	user.setType(jTable1.getValueAt(row, 4).toString());
            return user;
        }
        else
        	errorUser.setText("Select a row from table!");
        return null;
	}
