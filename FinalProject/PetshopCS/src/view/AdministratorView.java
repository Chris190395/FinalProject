	@Override
	public void SetUsersInGrid(ArrayList<User> usersList) {
		// TODO Auto-generated method stub
		String[][] obj = new String[usersList.size()][6];
    	for(int i=0;i<usersList.size();i++){
    			obj[i][0]=""+usersList.get(i).getId();
    			obj[i][1]=usersList.get(i).getUserName();
    			obj[i][2]=usersList.get(i).getPassword();
    			obj[i][3]=usersList.get(i).getFirstName();
    			obj[i][4]=usersList.get(i).getLastName();
    			obj[i][5]=usersList.get(i).getType();
    		
    	}
    	jTable1.setModel(new javax.swing.table.DefaultTableModel(
                obj,
                new String [] {
                        "Id", "UserName", "Password", "FirstName", "LastName", "Type"
                    }
            ));
	}

